    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:        
        if(txtNombre.getText().equals("")){
            JOptionPane.showMessageDialog(rootPane, "Campo nombre Obligatorio", "Informacion", JOptionPane.WARNING_MESSAGE);
            txtNombre.requestFocusInWindow();
            return;
        }
        
        if(txtUsuario.getText().equals("")){
            JOptionPane.showMessageDialog(rootPane, "Campo usuario Obligatorio", "Informacion", JOptionPane.WARNING_MESSAGE);
            txtUsuario.requestFocusInWindow();
            return;
        }
        
        String clave = new String(txtClave.getPassword());
        String confirma = new String(txtConfirmarClave.getPassword());
        if(clave.equals("")){
            JOptionPane.showMessageDialog(rootPane, "Campo clave Obligatorio", "Informacion", JOptionPane.WARNING_MESSAGE);
            txtClave.requestFocusInWindow();
            return;
        }
        
        if(confirma.equals("")){
            JOptionPane.showMessageDialog(rootPane, "Campo confirmar clave obligatorio", "Informacion", JOptionPane.WARNING_MESSAGE);
            txtConfirmarClave.requestFocusInWindow();
            return;
        }
        
        if (!clave.equals(confirma)){
            JOptionPane.showMessageDialog(rootPane, "Las claves no coinciden", "Informacion", JOptionPane.WARNING_MESSAGE);
            txtClave.setText("");
            txtConfirmarClave.setText("");
        }
        
        if(cmbPerfil.getSelectedIndex() == 0){
            JOptionPane.showMessageDialog(rootPane, "Campo perfil Obligatorio", "Informacion", JOptionPane.WARNING_MESSAGE);
            cmbPerfil.requestFocusInWindow();
            return;
        }
        
        String msg = null;
        if(txtID.getText().equals("")){
            DUsuarios dts = new DUsuarios();
            LUsuarios fun = new LUsuarios();
            
            String cla = new String(txtClave.getPassword());
            String perfil = String.valueOf(cmbPerfil.getSelectedItem());
            dts.setNombre(txtNombre.getText());
            dts.setUsuario(txtUsuario.getText());
            dts.setClave(cla);
            dts.setPerfil(perfil);
            msg = fun.insertarUsuarios(dts);
            if(msg == "si"){
                JOptionPane.showMessageDialog(rootPane, "Se registro de forma correcta" ,"Informacion", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(rootPane, "Ocurrio un problema","Informacion", JOptionPane.ERROR_MESSAGE);
            }
 
        } else {
            String ms = null;
            DUsuarios dts = new DUsuarios();
            LUsuarios fun = new LUsuarios();
            
            String cla = new String(txtClave.getPassword());
            String perfil = String.valueOf(cmbPerfil.getSelectedItem());
            dts.setIdUsuario(Integer.parseInt(txtID.getText()));
            dts.setNombre(txtNombre.getText());
            dts.setUsuario(txtUsuario.getText());
            dts.setClave(cla);
            dts.setPerfil(perfil);
            msg = fun.editarUsuarios(dts);
            if(msg == "si"){
                JOptionPane.showMessageDialog(rootPane, ms, "Se actualizo de forma correcta", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(rootPane, ms, "Ocurrio un problema", JOptionPane.ERROR_MESSAGE);
            }
        }
        limpiar();
        habilitar(true);
    }//GEN-LAST:event_btnGuardarActionPerformed

