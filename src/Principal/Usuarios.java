    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        if(!txtID.getText().equals("")){
            DUsuarios dts = new DUsuarios();
            LUsuarios fn = new LUsuarios();
            dts.setIdUsuario(Integer.parseInt(txtID.getText()));
            String msg = fn.eliminarUsuarios(dts);
            if(msg.equals("si")){
                JOptionPane.showMessageDialog(rootPane,"Se elimino de forma correcta","Informacion", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(rootPane, "Ocurrio un problema","Informacion", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Seleccione un usuario para eliminar","Informacion", JOptionPane.ERROR_MESSAGE);
        }
        limpiar();
        habilitar(true);
    }//GEN-LAST:event_btnEliminarActionPerformed

