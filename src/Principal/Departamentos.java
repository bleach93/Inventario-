    // Acciones del método eliminar
    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        if (!txtID.getText().equals("")) {
            DDepartamentos dts = new DDepartamentos();  // Referencia a la clase datos de departamento
            LDepartamentos fn = new LDepartamentos();   // Referencia a la clase lógica de departamento
            dts.setId(Integer.parseInt(txtID.getText()));
            String msg = fn.eliminarDepartamentos(dts);
            if (msg.equals("si")) {
                JOptionPane.showMessageDialog(rootPane, "Se elimino de forma correcta", "Informacion", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(rootPane, "Ocurrio un problema", "Informacion", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Seleccione un usuario para eliminar", "Informacion", JOptionPane.ERROR_MESSAGE);
        }
        habilitar(true);
        limpiar();
        mostrarBuscar("");
    }//GEN-LAST:event_btnEliminarActionPerformed
