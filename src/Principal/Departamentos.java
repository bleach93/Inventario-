    // Método para las acciones del botón guardar
    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        //Validaciones
        if (txtNombre.getText().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Campo nombre Obligatorio", "Informacion", JOptionPane.WARNING_MESSAGE);
            txtNombre.requestFocusInWindow();
            return;
        }

        String msg = null;
        if (txtID.getText().equals("")) {
            DDepartamentos dts = new DDepartamentos();
            LDepartamentos fun = new LDepartamentos();

            dts.setNombre(txtNombre.getText());
            msg = fun.insertarDepartamentos(dts);
            if (msg == "si") {
                JOptionPane.showMessageDialog(rootPane, "Se registro de forma correcta", "Informacion", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(rootPane, "Ocurrio un problema", "Informacion", JOptionPane.ERROR_MESSAGE);
            }

        } else {
            String ms = null;
            DDepartamentos dts = new DDepartamentos();  // Referencia a la clase datos de departamento
            LDepartamentos fun = new LDepartamentos(); // Referencia a la clase lógica de departamento

            dts.setId(Integer.parseInt(txtID.getText()));
            dts.setNombre(txtNombre.getText());
            msg = fun.editarDepartamentos(dts);
            if (msg == "si") {
                JOptionPane.showMessageDialog(rootPane, ms, "Se actualizo de forma correcta", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(rootPane, ms, "Ocurrio un problema", JOptionPane.ERROR_MESSAGE);
            }
        }
        limpiar();  // Llamada del método limpiar
        habilitar(true); // Llamada del método habilitar, en este caso en falso para deshabilitar el botón nuevo.
        mostrarBuscar(""); // Llama del método mostrarBuscar
    }//GEN-LAST:event_btnGuardarActionPerformed

